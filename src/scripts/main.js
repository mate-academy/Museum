'use strict';

const $ = window.$;

$(document).ready(function() {
  $('.header__button').click(function() {
    hamburgerSlide();
  });

  $('.hamburger__close-button').click(function() {
    hamburgerSlide();
  });

  $('.hamburger__nav-item').click(function() {
    hamburgerSlide();
  });
});

function hamburgerSlide() {
  $('.hamburger').slideToggle();
  $('.hamburger__nav').slideToggle();
}
